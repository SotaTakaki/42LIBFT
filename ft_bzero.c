#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*cast_s;
	int		i;

	cast_s = (char *)s;
	i = 0;
	while (i < n)
	{
		cast_s[i] = '\0';
		i++;
	}
}
